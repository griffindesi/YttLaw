            <h2>Attorney Mert Yalçın</h2>
<p>Mert Yalçın graduated from the Dokuz Eylül University Faculty of Law in 2001. As an active student, besides his studies, he was also involved in various organisations including being the President of ELSA’s (European Young Lawyers Association) branch in Izmir and on top of that he began to learn the secrets of the profession when he was still a student by completing the internship in various Global Law Firms.</p>
<p>In 2001, he was registered as a lawyer in the Izmir Bar Association and began his professional career in a leading law firm.</p>
<p>In 2002, he enhanced his knowledge by enrolling on various courses and lectures at the University of California, Los Angeles, in the USA.</p>
<p>After returning from the USA, he continued his legal career in Istanbul and became one of the first founders of the prominent leading law firm in its Istanbul branch.</p>
<p>During the year 2005, he had been active in providing legal consulting services to foreign capital enterprises and some Fortune 500 companies which had invested in Turkey in order to give information about new investments or either to be in pursuit of their respective works.<br>
Also, he has been a legal consultant in the investment and development projects of various real estate funds and has provided consultancy services to the leading construction firms. In addition, he has provided consultancy services to foreign natural individuals in the field of International Private Law.</p>
<p>Furthermore, in 2005, he was a guest lawyer at the Bates Wells Braithwaite Law Firm in London.<br>
In addition, he was also involved in one of the most important cases of Family Law in Turkey until today ‘the case of Chelouche v Eskinazi’ as the principal lawyer; by applying his international family law knowledge in addition to his corporate legal counselling expertise in this case.<br>
Moreover, he has provided legal services to clients from various nationalities as well.<br>
In 2008, Mert Yalçın was invited by the U.S Ministry of Interior (Department of State) and was awarded with a ‘Leadership Certificate’ on behalf of the American Embassy in Turkey for his successful works in the field of International Family Law especially for the works he performed on the International Child Abduction Cases in 5 states and on the 1980 Hague Convention with regards to the Legal Aspects of Child Abduction.<br>
During this period, in parallel with his commitments in International Family law, he significantly expanded his experience by engaging in the fields of corporate/company law in particular International Arbitration. As a result, he has been working as a lawyer in the most important international arbitration cases present in Turkey.</p>
<p>In 2011, Mert Yalçın incorporated with Kortan Toygar, the Yalçın &amp; Toygar Law Firm. Till this day they are both managing partners of the firm.<br>
Mert Yalçın has been acting as a participant as well as a speaker in many local and international conferences. Concurrently, he is still actively contributing to the AIJA (International Young Lawyers Association) (the association which he used to successfully administer and carry out activities in it as a representative of Turkey), through various commissions. Mert Yalçın also participates in IBA (International Bar Association) Meetings where he works with different committees.</p>
<p>Moreover, Mert Yalçın has been recognised as an expert pundit in courts of different countries such as United Kingdom, America (California, New York, Florida) Switzerland, Holland, Canada and France either by physically being present in the courts or by expressing his opinion in writing.</p>
<p>Also, Mert Yalçın has been directly serving as Legal Advisor in consulates and embassies of various nations.<br>
Mert Yalçın works as a great team leader when working with his colleagues and as a lawyer, his main principle is to provide top notch services to his clients by achieving the global standards.<br>
Besides his career as a lawyer, Mert Yalçın is a squash player and as part of his hobbies, he enjoys riding motorbikes, travelling and fishing with a fishing spear as well as, fishing rod.</p>
