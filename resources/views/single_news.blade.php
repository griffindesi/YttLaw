					<h2 class="post__title">The Communique (NO: 51\32 -2018) which made Amendments to the previous Communique (No:34\32-2008) which concerns the Protection of the Value of the Turkish Currency</h2>
				</header>

				<div class="post__content single-content">

					<p>&nbsp;</p>
<p><strong>ARTICLE 1 –</strong> Repealed Article 8 of the Communique (No: 2008-32 / 34) on the Protection of the Value of Turkish Currency published in the Official Gazette dated 28/2/2008 and numbered 26801 was revised as follows with the title.</p>
<p>Contracts in foreign currency or indexed to foreign currency</p>
<p><strong>ARTICLE 8 –</strong> (1) Residents in Turkey; cannot decide on the following contracts price and the other payment obligations arising from these contracts as based on foreign currency or indexed to foreign currency; the contracts which are related to real estates within the country including the free zones and related to real estate sales contracts including residential and roofed workplaces.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; (2)&nbsp; Residents in Turkey; cannot decide on the following contracts price and the other payment obligations arising from these contracts as based on foreign currency or indexed to foreign currency; the contracts which are related to real estates within the country including the free zones and related to real estate leasing contracts including residential and roofed workplaces.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (3)&nbsp; Residents in Turkey; cannot decide on the employment contract price signed between each other and the other payment obligations arising from these contracts, except for those to be fulfilled abroad, as based on foreign currency or indexed to foreign currency.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (4)&nbsp; Residents in Turkey; cannot decide on the contract for the consultancy contracts, brokerage contracts and service contracts including transportation-other than those specified below- and the other payment obligations arising from these contracts, as based on foreign currency or indexed to foreign currency.</p>
<ol>
<li>The service contracts which signed by the people who have not the citizenship of Turkish Republic</li>
<li>The service contracts signed within the scope of exports, transit trade, sales and deliveries considered as exports, and the foreign exchange earning services and activities,</li>
<li>Service contracts signed within the scope of the activities to be carried out abroad by residents in Turkey,</li>
<li>The service contracts which signed among the residents of Turkey related to electronic communication which starts in Turkey and ends abroad and starts abroad and ends in Turkey.</li>
</ol>
<p>(5)&nbsp; The residents of Turkey, cannot decide on the contracts except contracts of construction which defined in the Turkish International Ship Registry Law numbered 4490 and dated 16.12.1999 and which also defined in the Law Regarding to Amendments on the Decree Law Numbered 491 and which are related the construction, repairment and maintenance of the ship; and they cannot decide on the other payment obligations arising from these contracts which signed among the residents of Turkey, based on foreign currency or indexed to foreign currency.</p>
<p>(6)&nbsp; The contracts of sales of movable assets except of the vehicle sale contracts including construction equipment and the other payment obligations arising from these contracts which will be signed among the residents of Turkey, shall be decided as based on the foreign currency or indexed to foreign currency.</p>
<p>(7)&nbsp;&nbsp; The contracts of the leasing of movable assets except of the vehicle leasing contracts including construction equipment and the other payment obligations arising from these contracts which will be signed among the residents of Turkey, shall be decided based on the foreign currency or indexed to foreign currency.</p>
<p>(8)&nbsp;&nbsp;&nbsp;&nbsp; The sales contracts for software which produced abroad and license and service contracts for hardware and software within the scope of information technologies and other payment obligations arising from these contracts which will be signed among the residents of Turkey, shall be decided as based on the foreign currency or indexed to foreign currency.</p>
<p>(9)&nbsp;&nbsp;&nbsp;&nbsp; The financial leasing contracts for ships which defined in the Turkish International Ship Registry Law numbered 4490 and dated 16.12.1999 and which also defined in the Law Regarding to Amendments on the Decree Law Numbered 491 and other payment obligations arising from these contracts which will be signed among the residents of Turkey, shall be decided as based on the foreign currency or indexed to foreign currency.</p>
<p>(10)&nbsp; The leasing contracts to be made within the scope of Articles 17 and 17 / A of Decision No. 32 on the Protection of Turkish Currency Value, shall be based in foreign currency.</p>
<p>(11)&nbsp;&nbsp;&nbsp; The employment contracts and other payment obligations arising from these contracts which will be signed among the residents but non-citizens of Turkey, shall be decided as based on the foreign currency or indexed to foreign currency.</p>
<p>(12)&nbsp;&nbsp; The real estate sales contracts sand real estate leasing contracts and other payment obligations arising from these contracts, except of those signed by the state institutions and organizations and by the Turkish Armed Forces Foundation, shall be decided as based on foreign currency or indexed to foreign currency.</p>
<p>(13)&nbsp;&nbsp;&nbsp; The contracts except of the real estate sales, real estate leasing and employment and which signed by the contractor with the third parties and the other payment obligations arising from these contracts shall be decided based on foreign currency or indexed to foreign currency by condition of the fact that contracts shall be in the scope of fulfillment of the tenders, contracts in foreign currency or indexed to foreign currency and signed by the public institutions and organizations or shall be in the scope of fulfillment of international agreements signed by the public institutions and organizations.</p>
<p>(14)&nbsp;&nbsp; The contracts which signed by banks as to the transactions performed by the Ministry of Treasury within the scope of the Law on the Regulation of Public Finance and Debt Management dated 28/3/2002 and numbered 4749, and other payment obligations arising from these contracts, shall be decided based on the foreign currency or indexed to foreign currency.</p>
<p>(15)&nbsp;&nbsp; Without prejudice to the provisions of the Decree No:32 Protection of the Value of Turkish Currency, capital market instruments (including foreign capital market instruments and depositary receipt and foreign investment fund shares)- in accordance with the Capital Markets Law No. 6362 and the regulations made based on this Law, shall be formed in foreign currency; and the obligations as to the export, purchase-sale, and the transactions shall be decided based on the foreign currency.</p>
<p>(16)&nbsp;&nbsp; The service and employment contracts which signed by the branches, representations, liaison offices located in Turkey and belonging to the non-residents of Turkey, and signed by the companies which non-residents of Turkey are shareholder in at least fifty percent, and signed by the companies which are located in the free-zone areas and acting within the scope of free-zone business, and other payment obligations arising from these contracts, shall be decided based on the foreign currency or indexed to foreign currency.</p>
<p>(17)&nbsp;&nbsp; The contracts except of the real estate sales, real estate leasing, employment can be signed with residents of Turkey as based on the foreign currency by the commercial airline operations which transports passengers, loads and mails and residence in Turkey; by the companies providing technical maintenance services for air transport vehicles, engines and its parts; by the public or private entities which have work permit or have been authorized to perform ground handling services in airports within the scope of civil aviation legislation; by the operations or companies which established by the herein public or private entities, and by the partnerships herein public or private entities are shareholder in at least 50 percent (%) directly or indirectly.</p>
<p>(18)&nbsp;&nbsp; In accordance with this article, if it is not possible to determine the value of the contract in foreign currency or foreign currency indexed in accordance with the contract price and the other payment liabilities arising from these contracts; the negotiable instruments issued within these contracts shall not be determined based on the foreign currency or indexed to foreign currency.</p>
<p>(19)&nbsp; Contracts indexed to precious metals and / or commodities which price is determined as foreign currency in international markets and/ or contracts indexed to foreign currency indirectly; shall be considered as the contracts indexed to foreign currency within the scope of the Article/4g of the Decree No: 32 Protection of the Value of Turkish Currency.</p>
<p>(20)&nbsp;&nbsp;&nbsp; Branches, representations, offices, liaison offices located abroad and belonging to residents of Turkey, funds managed and operated abroad, the companies which residents of Turkey have at least fifty percent share in and the companies which residents of Turkey have ownership directly or indirectly; shall be considered as operated in Turkey in accordance with the Article/4g of the Decree No: 32 Protection of the Value of Turkish Currency.</p>
<p>(21)&nbsp;&nbsp;&nbsp; The contracts concluded before the date of enactment of the Provisional Article 8 of Decree No. 32 on the Protection of the Value of Turkish Currency and included the scope of the exemptions pursuant to this article, are also exempt from the herein Provisional Article.</p>
<p>(22)&nbsp; The contracts of the vehicle leasing including construction equipment concluded before the date of enactment of the Provisional Article 8 of Decree No. 32 on the Protection of the Value of Turkish Currency, are also exempt from the herein Provisional Article.</p>
<p>(23) The amount of the contract and the other payment obligations arising from these contracts, which are not possible to be determined in foreign currency or foreign currency indexed in accordance with this article, are required to be re-determined by the parties as Turkish lira as part of the Provisional Article 8 of the Decision No. 32 on the Protection of Turkish Currency Value.</p>
<p>(24)&nbsp; If the amount of the contract and the other payment obligations arising from these contracts, which are not possible to be determined in foreign currency or foreign currency indexed in accordance with this article, which required to be re-determined by the parties as Turkish lira as part of the Provisional Article 8 of the Decision No. 32 on the Protection of Turkish Currency Value; cannot be determined by the parties by reaching an agreement; the costs of the concluded contracts which specified in foreign currency or indexed to foreign currency, shall be determined&nbsp; by increasing of the value of the Turkish Lira calculated based on the indicative effective selling rate dated 2.01.2018 of Central Bank of the Republic of Turkey by taking into account the basis of monthly exchange rate of consumer price index determined by the Turkey Statistics Institution from the date of 02.01.2018 to date of which costs redetermined.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The amounts determined as foreign currency or foreign currency indexed in the housing and roofed office leasing contracts concluded before the date of enactment of the Provisional Article 8 of the Decree No. 32 on the Protection of the Value of Turkish Currency, shall be determined as Turkish Lira for a period of two years in accordance with the first paragraph of this sub-article. However, if the parties cannot reach an agreement on the rental price which shall be redetermined as Turkish Lira to be valid for 1 year starting from the end of contract year which re-determination has been performed in accordance with the mentioned paragraph, the rental price shall be determined by taking into account the basis of monthly exchange rate of consumer price index determined by the Turkey Statistics Institution from the date of determination to end of the year which determination has been performed. If the rental price for the next rental year based on Turkish Lira, and if the parties cannot reach an agreement throughout the redetermination; the rental price shall be determined by increasing previous year’s rental price by taking into account the basis of monthly exchange rate of consumer price index determined by the Turkey Statistics Institution and redetermined rental price shall be valid until the end of 2 years which stated in this paragraph.</p>
<p>The provisions of this paragraph shall not apply to collected receivables or receivables in arrier within the contracts which cannot be subjected to foreign currency or foreign indexed currency in terms of the contract value and other payments arising from these contracts.</p>
<p>&nbsp;(25)&nbsp; The value of contract shall be determined as Turkish Lira in case of the existence of the agreement of the parties who have been exempted ; on the matter that the new contracts will be based on Turkish Lira or the amounts based on the foreign currency or foreign currency indexed will be converted to Turkish Lira in accordance with the Provisional Article 8 Decree No:32 Protection of the Value of Turkish Currency.</p>
<p>&nbsp;</p>
<p><strong>ARTICLE 2 –</strong> This Communique become into force on the date of publication.</p>
<p>&nbsp;</p>
<p><strong>ARTICLE 3 –</strong> The provisions of this Communiqué are executed by the Minister of Treasury and Finance.</p>
